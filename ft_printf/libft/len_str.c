#include "libft.h"

int	ft_len_str(int carac, int *flag)
{
	static int	j;
	int			len;

	len = 0;
	if (carac == -1)
	{
		j = 1;
		*flag = 1;
		return (-1);
	}
	if (!j)
		j = 1;
	len = (carac - 48) * j;
	j *= 10;
	return (len);
}
